DROP TABLE IF EXISTS day;
CREATE TABLE IF NOT EXISTS day (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  [name] TEXT(9),
  [value] INTEGER
);

DROP TABLE IF EXISTS exam_period;
CREATE TABLE IF NOT EXISTS exam_period (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  [name] TEXT(11),
  [value] INTEGER
);

DROP TABLE IF EXISTS department;
CREATE TABLE IF NOT EXISTS department (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  [name] TEXT(50),
  [value] INTEGER
);

DROP TABLE IF EXISTS schedule_type;
CREATE TABLE IF NOT EXISTS schedule_type (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  [name] TEXT(13),
  [value] INTEGER
);

DROP TABLE IF EXISTS semester_type;
CREATE TABLE IF NOT EXISTS semester_type (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  [name] TEXT(50),
  [value] INTEGER
);

DROP TABLE IF EXISTS subject_type;
CREATE TABLE IF NOT EXISTS subject_type (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  [name] TEXT(11),
  [value] INTEGER
);

DROP TABLE IF EXISTS teacher_title;
CREATE TABLE IF NOT EXISTS teacher_title (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  [name] TEXT(50),
  [value] INTEGER
);

DROP TABLE IF EXISTS teacher;
CREATE TABLE IF NOT EXISTS teacher (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  firstName TEXT(50),
  lastName TEXT(50),
  phone TEXT(10),
  titleValue INTEGER,
  departmentValue INTEGER,
  email TEXT(50),
  FOREIGN KEY(departmentValue) REFERENCES department([value])
  FOREIGN KEY(titleValue) REFERENCES teacher_title([value])
);

DROP TABLE IF EXISTS subject;
CREATE TABLE IF NOT EXISTS subject (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  code TEXT(10),
  [name] TEXT(65),
  subjectTypeValue INTEGER,
  departmentValue INTEGER,
  semesterTypeValue INTEGER,
  credits REAL,
  FOREIGN KEY(subjectTypeValue) REFERENCES subject_type([value]),
  FOREIGN KEY(departmentValue) REFERENCES department([value]),
  FOREIGN KEY(semesterTypeValue) REFERENCES semester_type([value])
);

DROP TABLE IF EXISTS semester;
CREATE TABLE IF NOT EXISTS semester (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  semesterTypeValue INTEGER,
  startDate INTEGER,
  endDate INTEGER,
  maxCredits REAL,
  FOREIGN KEY(semesterTypeValue) REFERENCES semester_type([value])
);

DROP TABLE IF EXISTS schedule;
CREATE TABLE IF NOT EXISTS schedule (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  startTime INTEGER,
  endTime INTEGER,
  room TEXT(50),
  dayValue INTEGER,
  scheduleTypeValue INTEGER,
  FOREIGN KEY(dayValue) REFERENCES day([value]),
  FOREIGN KEY(scheduleTypeValue) REFERENCES schedule_type([value])
);

DROP TABLE IF EXISTS exam;
CREATE TABLE IF NOT EXISTS exam (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  score REAL,
  subjectId INTEGER,
  scheduleId INTEGER,
  semesterId INTEGER,
  date INTEGER,
  examPeriodValue INTEGER,
  FOREIGN KEY(subjectId) REFERENCES subject(id),
  FOREIGN KEY(scheduleId) REFERENCES schedule(id),
  FOREIGN KEY(semesterId) REFERENCES semester(id),
  FOREIGN KEY(examPeriodValue) REFERENCES exam_period(value)
);

DROP TABLE IF EXISTS course;
CREATE TABLE IF NOT EXISTS course (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  colorValue INTEGER,
  subjectId INTEGER,
  semesterId INTEGER,
  teacherId INTEGER,
  FOREIGN KEY(subjectId) REFERENCES subject(id),
  FOREIGN KEY(semesterId) REFERENCES semester(id),
  FOREIGN KEY(teacherId) REFERENCES teacher(id)
);

DROP TABLE IF EXISTS lecture;
CREATE TABLE IF NOT EXISTS lecture (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  courseId INTEGER,
  scheduleId INTEGER,
  FOREIGN KEY(courseId) REFERENCES course(id),
  FOREIGN KEY(scheduleId) REFERENCES schedule(id)
);

